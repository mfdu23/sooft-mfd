package com.soofttech.soof.util;

import java.util.Date;

public class DtoEmpresa {
	private String cuil;
	private String razonSocial;
	private Date fechaAdesion;
	public String getCuil() {
		return cuil;
	}
	public void setCuil(String cuil) {
		this.cuil = cuil;
	}
	public String getRazonSocial() {
		return razonSocial;
	}
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}
	public Date getFechaAdesion() {
		return fechaAdesion;
	}
	public void setFechaAdesion(Date fechaAdesion) {
		this.fechaAdesion = fechaAdesion;
	}
	
	
}
